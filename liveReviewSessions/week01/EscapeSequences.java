package week01;

public class EscapeSequences {

    public static void main(String[] args) {

        /*
        \t: tab
        \n: takes it to new line
        \\: prints \
        \*: prints *
         */

        System.out.println("Learn\tJava\n\tthe\nHard\tWay\n\n");
        System.out.println("\t Learn JAVA the \" EASY WAY! \" \n\t\t With Oscar \\\\");

    }


}
