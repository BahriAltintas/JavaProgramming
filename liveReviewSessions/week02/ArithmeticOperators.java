package week02;

public class ArithmeticOperators {
    public static void main(String[] args) {
        /*
        Division
        * in math 10/3 = 3,333333..
        *in Java 10/3 = 3 reason int/int=int ;
        *10/3.0 int/double = double
        *10.0/3 double/int = double
         */

        System.out.println("10/3= "+(10/3));
        System.out.println("10/3.0= "+(10/3.0)); //3.0 is double data type
        System.out.println("10 +3 = "+10+3); //10+3 =103 concatenation: String+int=String "10+3 = " + 10

        // Which operations come first in JAVA
        // parantheses comes first, division, multiplication is before concatenation





    }

}
